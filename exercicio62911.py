numero = int(input('Digite um numero: '))

if(numero==5):
    print('O valor eh igual; ')

if(numero!=7):
    print('O valor eh diferente de 7; ')

if(numero>2):
    print('O valor eh maior que 2; ')

if(numero>=5):
    print('O valor eh maior ou igual a 5; ')

if(numero>7):
    print('O valor eh maior que 7; ')
else:
    print('O valor eh menor que 7; ')

if(numero<=5):
    print('O valor eh menor ou igual a 5;')